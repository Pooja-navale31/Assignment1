package Java_Assignment;

public class Comparision_Logical_Operation {
    public static void main(String[] args) {
        // initializing variables
        int a = 10, b = 5;

        System.out.println(a==b);
        System.out.println(a>b);
        System.out.println(a<b);
        System.out.println((a > b) && (a > 0));
        System.out.println((a < b) || (a > 0));
        System.out.println(!(a > b));

}
    }

